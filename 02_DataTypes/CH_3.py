# Calculate the number of tea bags per pot using integer division
bags_per_pot = total_tea_bags // pots  # Divides tea bags by pots, keeps only the whole number part (integer division)

# Print the number of tea bags per pot
print("Tea bags per pot:", bags_per_pot)  # Displays how many tea bags go into each pot


# Assign the total number of cardamom pods
