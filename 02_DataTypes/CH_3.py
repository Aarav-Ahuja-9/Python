# Assign the price of black tea to a variable
black_tea_price = 20  # The price of black tea is set to 20 and stored in 'black_tea_price'

# Assign the price of ginger tea to a variable
giger_tea_price = 25  # The price of ginger tea is set to 25 and stored in 'giger_tea_price'

# Calculate the total price of both teas
Total_tea_price = black_tea_price + giger_tea_price  # Adds the prices of black and ginger tea, result stored in 'Total_tea_price'

# Print the total tea price
print("Total tea price:", Total_tea_price)  # Displays the combined price of both teas


# Subtract 10 from the total tea price to apply a discount
remnaining_tea_price = Total_tea_price - 10  # Applies a discount of 10 to the total tea price, stores the result in 'remnaining_tea_price'

# Print the remaining tea price after discount
print("Remaining tea price after discount:", remnaining_tea_price)  # Displays the price after discount


